# for x in y:
#   z
#

#   x => Yeni döngüden kaydedilecek değerleri kaydeen değişken. Genellikle i yazılırç
#   y => Döndürülecek değer.
#   z => İşlem yeri.
#
# For : Bir listenin veya birden fazla değere sahip olan değişkenin hepsinin sırayla fordaki z bölgenin işlemlerini yapması için teker teker döndürür.
# Z bölgesine bir şey yazmadığınızda hata verir.

# RANGE

for i in range(10):
    print(i)

for i in range(1,10):
    print(i)

for i in range(1,10,2):
    print(i)
