# string = str = "x"                => x = Her hangi bir yazı
# integer = int = x                 => x = Her hangi bir sayı

a = "yazi"   # => str
b = "7"      # => str
c = 7        # => int
d = c        # => değişken

# Değişkenlerin hangi tür olduğunu bilmek için type fonksiyonu kullanılır.

print("A değişkeni : ", type(a))
print("B değişkeni : ", type(b))
print("C değişkeni : ", type(c))
print("D değişkeni : ", type(d))

